// Keywords and Reserved Words
/*
  Keywords are reserved words that have special meanings in JavaScript.
  Reserved words are words that are reserved for future use in JavaScript.
  
  let,      -> let used in lexical structure to declare variables
  const,    -> const used in lexical structure to declare constants
  var,      -> var used in lexical structure to declare variables
  if,       -> if used in lexical structure to declare conditional statements
  else,     -> else used in lexical structure to declare conditional statements
  for,      -> for used in lexical structure to declare loops
  while,    -> while used in lexical structure to declare loops
  function, -> function used in lexical structure to declare functions
  return,   -> return used in lexical structure to declare functions
  class,    -> class used in lexical structure to declare classes
  import,   -> import used in lexical structure to declare modules
  export,   -> export used in lexical structure to declare modules
  async,    -> async used in lexical structure to declare asynchronous functions
  await,    -> await used in lexical structure to declare asynchronous functions
  break,    -> break used in lexical structure to declare loops
  continue, -> continue used in lexical structure to declare loops
  
*/

// Punctuators and Operators
/*
Used to Form Expression

+           ->  Addition or string concatenation.
-           ->  Subtraction.
*           ->  Multiplication.
/           ->  Division.
=           ->  Assignment operator (assigns a value to a variable).
===         ->  Strict equality (checks value and type).
!==         ->  Strict inequality (checks if value or type differ).
&&          ->  Logical AND (returns the first falsy value or last truthy).
||          ->  Logical OR (returns the first truthy value or last falsy).
? :         ->  Ternary operator (shorthand for if-else: condition ? expr1 : expr2).
;           ->  Ends a statement.
,           ->  Separates items in a list (e.g., function arguments or array elements).
., [], {}   ->  Access object properties (. or []) or define objects ({}) and arrays ([]).
()          ->  Group expressions or define function parameters and calls.
=>          ->  Arrow function syntax (a concise way to write functions).

*/