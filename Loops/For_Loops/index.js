// For Loops used when the number of iterations is known or can be determined at runtime.
for (let i = 0; i < 5; i++) {
  console.log(i);
}
// Initialization
let i = 0; // like in Variables we can initialize a variable with a value inside the for loop
// Condition
i < 5;     // the condition is checked before each iteration
// Increment
i++;       // the increment is performed after each iteration
